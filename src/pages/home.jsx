function Home() {
    const [currentSlide, setCurrentSlide] = useState(0);
    const [activeContent, setActiveContent] = useState(1);

    const slides = [sliderimg1, sliderimg2, sliderimg3];

    useEffect(() => {
        const interval = setInterval(() => {
            setCurrentSlide(prev => (prev === slides.length - 1 ? 0 : prev + 1));
        }, 5000);

        return () => clearInterval(interval);
    }, [slides.length]);

    const renderContent = () => {
        switch (activeContent) {
            case 1: return <Content1 />;
            case 2: return <Content2 />;
            case 3: return <Content3 />;
            case 4: return <Content4 />;
            default: return <Content1 />;
        }
    };

    return (
        <div className="home-page">
            {/* Слайдер */}
            <div className="slider-container">
                {slides.map((slide, index) => (
                    <div
                        key={index}
                        className={`slide ${index === currentSlide ? 'active' : ''}`}
                        style={{
                            backgroundImage: `url(${slide})`,
                            width: '100%',
                            height: '100%',
                            objectFit: 'cover'
                        }}
                    />
                ))}
            </div>

            {/* Кнопки для смены контента */}
            <div className="content-buttons">
                <button onClick={() => setActiveContent(1)}>О компании</button>
                <button onClick={() => setActiveContent(2)}>Услуги</button>
                <button onClick={() => setActiveContent(3)}>Клиенты</button>
                <button onClick={() => setActiveContent(4)}>Партнеры</button>
            </div>

            {/* Отображаемый контент */}
            <div className="dynamic-content">
                {renderContent()}
            </div>

            <CookieConsent />
        </div>
    );
}

export default Home;